package com.start;

public class Launch {

	public static void main(String[] args) {
		// WHILE LOOP
		int i = 1;
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		
		int a = 20;
		while(a > 9) {
			System.out.println(a);
			a--;
		}
		
		
		// DO WHILE LOOP
		int j = 100;
		do{
			System.out.println(j);
			j++;
		}while(j <= 110);

	}

}
