package com.interfaceinjava;

public class Thar implements MyInterface{
	
	@Override
	public void speed() {
		System.out.println("THAR SPEED");
	}
	
	@Override
	public void milage() {
		System.out.println("THAR MILAGE");
	}
	
	@Override
	public void engine() {
		System.out.println("THAR ENGINE");
	}
	
	@Override
	public void color() {
		System.out.println("THAR COLOR \n");
	}
}
